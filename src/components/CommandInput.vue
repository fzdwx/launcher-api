<template>
  <div command-raycast-top-shine=""/>
  <div class="command-input-container">
    <slot name="head"></slot>
    <input
        ref="inputRef"
        command-input=""
        auto-complete="off"
        auto-correct="off"
        :spell-check="false"
        aria-autocomplete="list"
        role="combobox"
        :aria-expanded="true"
        :placeholder="placeholder"
        :value="modelValue"
        @input="handleInput"
        class="command-input"
    />
    <slot name="tail"></slot>
  </div>
  <hr v-if="props.loading" class="command-raycast-loader-common" command-raycast-loader="">
  <hr v-else class="command-raycast-loader-common"/>
</template>

<script lang="ts">
import {defineComponent, ref, watchEffect} from 'vue'

export default defineComponent({
  name: 'Command.Input'
})
</script>

<script lang="ts" setup>
import {useCommandEvent, useCommandState} from "../composables";

const props = defineProps<{
  placeholder: string
  modelValue?: string
  disableFilter?: boolean
  loading?: boolean
}>();

const emit = defineEmits<{
  (e: 'input', ie: InputEvent): void
  (e: 'update:modelValue', val: any): void
}>()

const inputRef = ref()
const {search} = useCommandState()

const handleInput = (e: Event) => {
  const event = e as InputEvent
  const input = e.target as HTMLInputElement
  if (!props.disableFilter) {
    search.value = input?.value
  }

  emit('input', event)
  emit('update:modelValue', input.value)
}

const {emitter} = useCommandEvent();

emitter.on('setInputValue', (value: string) => {
  if (!props.disableFilter) {
    search.value = value;
  }
  emit('update:modelValue', search.value)
})

window.onfocus = () => {
  inputRef.value?.focus()
}

watchEffect(() => {
  inputRef.value?.focus()
})

</script>
